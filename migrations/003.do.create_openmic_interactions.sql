SET TIME ZONE 'UTC';

CREATE TABLE openmic_interactions (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  comment TEXT NOT NULL,
  date_created TIMESTAMP DEFAULT now() NOT NULL,
  rating INTEGER NOT NULL,
  user_id INTEGER
    REFERENCES openmic_users(id) ON DELETE CASCADE NOT NULL,
  media_id INTEGER
    REFERENCES media(id) ON DELETE CASCADE NOT NULL
);